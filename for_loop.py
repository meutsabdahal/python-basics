# using for loop to print out different letter in string variable
for letter in 'Python':
    print(letter)

# for loop for printing each element of that array
names = ['Utsab', 'John', 'Harry', 'Dahal']
for name in names:
    print(name)

# for loop for series of number/ printing Hello World multiple times
for index in range(5, 10):
    print(str(index) + '. Hello World!!')